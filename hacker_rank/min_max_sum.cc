void miniMaxSum(vector<int> arr) {
        sort(arr.begin(), arr.end()); 
        size_t min_sum = 0, max_sum = 0; 
        for (size_t i=0; i < arr.size(); i++) {
                if (i == 0)
                        min_sum += arr[i];
                else if (i == arr.size()-1)
                        max_sum += arr[i];
                else {
                        min_sum += arr[i];
                        max_sum += arr[i];
                }
        }
        printf("%lu %lu\n", min_sum, max_sum);
}

